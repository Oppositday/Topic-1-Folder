
public class Q01_08 {

	public static void main(String[] args) {
		double radius;
		double area;
		double perimeter;
		
		radius = 5.5;
		
		area = radius * radius * 3.14159;
		
		perimeter = 2 * radius * 3.14159;
		
		System.out.println("The area for the circle of radius " +
		radius + " is " + area);
		
		System.out.println("The perimeter for the circle of radius " +
		radius + " is " + perimeter);

	}

}
